package com.xl.ye.bean;

import java.math.BigDecimal;

public class PubFee {
	private int certNo;
	
	private String certType;
	
	private String accDate;
	
	private String accCode;
	
	private String subject;
	
	private BigDecimal amount;
	
	private String centCost;
	
	private String orderNo;
	
	private String centProfit;
	
	private String mark;
	
	private String negativeNo;
	
	private String reserve;

	public int getCertNo() {
		return certNo;
	}

	public void setCertNo(int certNo) {
		this.certNo = certNo;
	}

	public String getCertType() {
		return certType;
	}

	public void setCertType(String certType) {
		this.certType = certType;
	}

	public String getAccDate() {
		return accDate;
	}

	public void setAccDate(String accDate) {
		this.accDate = accDate;
	}

	public String getAccCode() {
		return accCode;
	}

	public void setAccCode(String accCode) {
		this.accCode = accCode;
	}

	public String getSubject() {
		return subject;
	}

	public void setSubject(String subject) {
		this.subject = subject;
	}

	public BigDecimal getAmount() {
		return amount;
	}

	public void setAmount(BigDecimal amount) {
		this.amount = amount;
	}

	public String getCentCost() {
		return centCost;
	}

	public void setCentCost(String centCost) {
		this.centCost = centCost;
	}

	public String getOrderNo() {
		return orderNo;
	}

	public void setOrderNo(String orderNo) {
		this.orderNo = orderNo;
	}

	public String getCentProfit() {
		return centProfit;
	}

	public void setCentProfit(String centProfit) {
		this.centProfit = centProfit;
	}

	public String getMark() {
		return mark;
	}

	public void setMark(String mark) {
		this.mark = mark;
	}

	public String getNegativeNo() {
		return negativeNo;
	}

	public void setNegativeNo(String negativeNo) {
		this.negativeNo = negativeNo;
	}

	public String getReserve() {
		return reserve;
	}

	public void setReserve(String reserve) {
		this.reserve = reserve;
	}
	
	

}
